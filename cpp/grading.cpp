#include<bits/stdc++.h>
using namespace std;

int main() {
    int a, b, c, d, e, g;
    cin >> a >> b >> c >> d >> e >> g;
    if (g >= a) cout << "A" << endl;
    else if (g >= b) cout << "B" << endl;
    else if (g >= c) cout << "C" << endl;
    else if (g >= d) cout << "D" << endl;
    else if (g >= e) cout << "E" << endl;
    else cout << "F" << endl;
}
